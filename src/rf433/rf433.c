/**********************************************************************************************************************
 * RF433 接收模块检测驱动 - 实现
 *
 * 原理：
 *   433MHz OOK/ASK 接收模块在收到 RF 载波时，数据引脚输出解调后的数字信号。
 *   RF 脉冲通常持续几十~几百毫秒，位宽约 100μs~1ms。
 *   本驱动在 10ms 窗口内以 50μs 间隔密集采样（约 200 次），
 *   一旦采到高电平即判定有信号，确保不错过短脉冲。
 **********************************************************************************************************************/
#include "hal_data.h"
#include "rf433.h"

/* 采样参数 */
#define SCAN_WINDOW_US      10000   /* 总扫描窗口：10ms */
#define SCAN_INTERVAL_US    50      /* 采样间隔：50μs */
#define SCAN_SAMPLES        (SCAN_WINDOW_US / SCAN_INTERVAL_US)  /* 200 次 */

/**********************************************************************************************************************
 * rf433_init
 * 预留初始化（当前仅用于扩展，比如配置上拉）
 **********************************************************************************************************************/
void rf433_init(void)
{
    /* 可在此配置内部上拉，确保无信号时引脚状态确定 */
    /* R_IOPORT_PinCfg(&g_ioport_ctrl, BSP_IO_PORT_07_PIN_07,
     *                IOPORT_CFG_PULLUP_ENABLE); */
}

/**********************************************************************************************************************
 * rf433_scan
 * 在 10ms 窗口内快速采样 P707 引脚，检测 RF 信号
 * 返回 1：检测到信号；0：无信号
 **********************************************************************************************************************/
int rf433_scan(void)
{
    bsp_io_level_t level;

    /* 密集采样 200 次（50μs间隔），总耗时约10ms */
    for (int i = 0; i < SCAN_SAMPLES; i++)
    {
        R_IOPORT_PinRead(&g_ioport_ctrl, BSP_IO_PORT_07_PIN_07, &level);

        if (BSP_IO_LEVEL_HIGH == level)
        {
            return 1;   /* 捕获到 RF 脉冲 */
        }

        R_BSP_SoftwareDelay(SCAN_INTERVAL_US, BSP_DELAY_UNITS_MICROSECONDS);
    }

    return 0;           /* 窗口内未检测到信号 */
}
